def draw_letter(dwg, ch):
    g = dwg.g(stroke="black", fill="none", stroke_width=STROKE_WIDTH)
    lines = letters_lines.get(ch, [])
    for x1, y1, x2, y2 in lines:
        g.add(dwg.line((x1, y1), (x2, y2)))
    return g

def main():
    for ch in letters_lines.keys():
        filename = os.path.join(output_dir, f"{ch if ch.isalnum() else 'sym_'+str(ord(ch))}.svg")
        dwg = svgwrite.Drawing(filename, size=(WIDTH, HEIGHT))
        dwg.add(draw_letter(dwg, ch))
        dwg.save()
    print("✅ 完全オリジナルな図形を letters/ に保存しました。")

if __name__ == "__main__":
    main()
